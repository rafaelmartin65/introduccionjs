// Null y Undefined
// En JavaScript existen diferentes tipos de datos 
// primitivos, además de string y number
// se tienen boolean que son verdadero y falso
// existe un par más llamados null y undefined
let numero;
console.log(numero);

let numero2 = null;
console.log(numero2);
console.log(typeof(numero2));


// Comparando null y undefined - Compara un valor null 
// y undefined pede se de las cosa más complicadas en JavaScript
// para ello llega a ser muy útil el comparador estricto

console.log("comparador estricto - numero === numero2", numero === numero2);
